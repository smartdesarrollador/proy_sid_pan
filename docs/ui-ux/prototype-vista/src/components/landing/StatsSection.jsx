const getBgClass = (template) => {
  switch (template) {
    case 'creative':
      return 'bg-gradient-to-r from-purple-600 to-pink-600 text-white';
    case 'dark':
      return 'bg-gray-900 border-t border-gray-700 text-gray-100';
    case 'minimal':
      return 'bg-gray-100 text-gray-900';
    default:
      return 'bg-blue-600 text-white';
  }
};

export const StatsSection = ({ content, template = 'corporate' }) => {
  const { title, items = [] } = content;

  const bgClass = getBgClass(template);
  const isLight = template === 'minimal';

  return (
    <section className={`py-16 px-6 ${bgClass}`}>
      <div className="max-w-5xl mx-auto">
        {title && (
          <h2 className={`text-3xl font-bold text-center mb-12 ${isLight ? 'text-gray-900' : 'text-white'}`}>
            {title}
          </h2>
        )}

        <div className={`grid gap-8 ${
          items.length <= 2
            ? 'grid-cols-1 sm:grid-cols-2'
            : items.length === 3
            ? 'grid-cols-1 sm:grid-cols-3'
            : 'grid-cols-2 lg:grid-cols-4'
        }`}>
          {items.map((stat, index) => (
            <div key={index} className="text-center">
              {stat.icon && (
                <div className={`flex justify-center mb-3 ${isLight ? 'text-gray-600' : 'text-white/70'}`}>
                  {getIcon(stat.icon)}
                </div>
              )}
              <div className={`text-4xl md:text-5xl font-extrabold mb-2 ${isLight ? 'text-gray-900' : 'text-white'}`}>
                {stat.value}
              </div>
              <div className={`text-sm md:text-base font-medium ${isLight ? 'text-gray-600' : 'text-white/80'}`}>
                {stat.label}
              </div>
            </div>
          ))}
        </div>
      </div>
    </section>
  );
};
